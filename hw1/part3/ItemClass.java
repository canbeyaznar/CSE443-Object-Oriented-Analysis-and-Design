/**
 *
 * This class will hold 2 parameters price and weight
 * @author Can BEYAZNAR
 */
public class ItemClass {

    private double price;
    private double weight;

    public ItemClass(double price, double weight){
        this.price = price;
        this.weight = weight;
    }

    public double getPrice() {
        return price;
    }

    public double getWeight() {
        return weight;
    }
}
